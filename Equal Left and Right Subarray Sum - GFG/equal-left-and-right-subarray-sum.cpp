class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
         if(N == 1)
         return N;
        int left = A[0],right = A[N-1];
        int i=1,j=N-2;
        while(i<=j)
        {
            if(left == right and i == j)
              return i+1;
            else if(left<right)
             left += A[i++];
            else 
             right += A[j--];
        }
        return -1; // code here
    }
};


